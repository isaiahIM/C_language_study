#include <stdio.h>
#include <stdlib.h>

#define BYTE char
/* run this program using the console pauser or add your own getch, system("pause") or input loop */


void PrintByte(BYTE byte);
void Reset(int *Count);

int main(int argc, char *argv[]) {
	
	int i, j, k, num=0, sw=1, loop;
	char Fnd[10][8]=
	{
		{0x00, 0x38, 0x44, 0x4c, 0x54, 0x64, 0x44, 0x38},// 0
		{0x00, 0x10, 0x30, 0x50, 0x10, 0x10, 0x10, 0x7c},// 1
		{0x00, 0x38, 0x44, 0x04, 0x08, 0x10, 0x20, 0x7c},// 2
		{0x00, 0x38, 0x44, 0x04, 0x18, 0x04, 0x44, 0x38},// 3
		{0x00, 0x08, 0x18, 0x28, 0x48, 0x7c, 0x08, 0x08},// 4
		{0x00, 0x7c, 0x40, 0x78, 0x04, 0x04, 0x44, 0x38},// 5
		{0x00, 0x38, 0x40, 0x40, 0x78, 0x44, 0x44, 0x38},// 6
		{0x00, 0x7c, 0x04, 0x08, 0x10, 0x20, 0x20, 0x20},// 7
		{0x00, 0x38, 0x44, 0x44, 0x38, 0x44, 0x44, 0x38},// 8
		{0x00, 0x38, 0x44, 0x44, 0x3c, 0x04, 0x44, 0x38},// 9
	};

	while(1)
	{
		if(num>9) 
		{
			Reset(&num);
		}
		
		for(k=0; k<10; k++)//숫자 교체 
		{
			for(loop=0; loop<2; loop++)//loop(dot점등, 소둥) 
			{	
				for(i=0;i<8;i++)//숫자 출력 
				{
					PrintByte(Fnd[num][i]);// 십의자리 
				
					PrintByte(Fnd[k][i]);// 일의자리 
					
					if( (i==7) && (sw==1) )// dot sw 
					{
						printf("*");
					}
					
					printf("\n");
				}
				
				sw*=-1;
				Sleep(500);
				system("cls");
			}
		}
		num++;
	} 	
	return 0;
}

void Reset(int *Count)
{
	*Count=0;
}

void PrintByte(BYTE byte)
{
	int j; 
	
	for(j=0;j<8;j++)//일의자리 비트 
	{
		if( byte & (0x80 >> j) )
		{
			printf("*");
		}
			
		else
		{
			printf(" ");	
		}
	}
}


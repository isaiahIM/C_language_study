void PrintByte(BYTE byte)// 산술연산자 
{
	int j; 
	
	for(j=0;j<8;j++)//일의자리 비트 
	{
		if( byte & (0x80 >> j) )
		{
			printf("*");
		}
			
		else
		{
			printf("-");	
		}
	}
}

